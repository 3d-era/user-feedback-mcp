def markdown_to_html(markdown_text: str) -> str:
    """Convert markdown to HTML with proper formatting support"""
    import re

    lines = markdown_text.split('\n')
    html_lines = []
    in_code_block = False
    code_block_content = []
    in_list = False

    for line in lines:
        # Code blocks
        if line.strip().startswith('```'):
            if in_code_block:
                # End code block
                code_content = '\n'.join(code_block_content)
                html_lines.append(f'<pre style="background-color: #1e1e1e; padding: 8px; margin: 5px 0; border-radius: 3px; color: #d4d4d4; font-family: monospace; font-size: 10pt;"><code>{code_content}</code></pre>')
                code_block_content = []
                in_code_block = False
            else:
                # Start code block
                in_code_block = True
            continue

        if in_code_block:
            code_block_content.append(line)
            continue

        # Headers
        if line.startswith('### '):
            html_lines.append(f'<h3 style="color: #3498db; margin: 8px 0 4px 0; font-size: 13pt;">{line[4:]}</h3>')
            continue
        elif line.startswith('## '):
            html_lines.append(f'<h2 style="color: #3498db; margin: 10px 0 5px 0; font-size: 14pt;">{line[3:]}</h2>')
            continue
        elif line.startswith('# '):
            html_lines.append(f'<h1 style="color: #3498db; margin: 12px 0 6px 0; font-size: 16pt;">{line[2:]}</h1>')
            continue

        # Lists
        if line.strip().startswith('- '):
            if not in_list:
                html_lines.append('<ul style="margin: 3px 0; padding-left: 20px;">')
                in_list = True
            content = line.strip()[2:]
            # Process inline formatting
            content = re.sub(r'\*\*(.+?)\*\*', r'<b>\1</b>', content)
            content = re.sub(r'`(.+?)`', r'<code style="background-color: #1e1e1e; padding: 1px 4px; border-radius: 2px; color: #d4d4d4; font-size: 10pt;">\1</code>', content)
            html_lines.append(f'<li style="margin: 2px 0; font-size: 11pt;">{content}</li>')
            continue
        else:
            if in_list:
                html_lines.append('</ul>')
                in_list = False

        # Empty lines
        if not line.strip():
            html_lines.append('<br>')
            continue

        # Regular text with inline formatting
        processed_line = line
        # Bold
        processed_line = re.sub(r'\*\*(.+?)\*\*', r'<b>\1</b>', processed_line)
        processed_line = re.sub(r'__(.+?)__', r'<b>\1</b>', processed_line)
        # Italic
        processed_line = re.sub(r'\*([^*]+?)\*', r'<i>\1</i>', processed_line)
        # Inline code
        processed_line = re.sub(r'`(.+?)`', r'<code style="background-color: #1e1e1e; padding: 1px 4px; border-radius: 2px; color: #d4d4d4; font-size: 10pt;">\1</code>', processed_line)

        html_lines.append(f'<span style="font-size: 11pt;">{processed_line}</span>')

    # Close any open list
    if in_list:
        html_lines.append('</ul>')

    html_content = ''.join(html_lines)

    return f'<div style="color: #ecf0f1; font-family: Arial, sans-serif; font-size: 11pt; line-height: 1.5;">{html_content}</div>'

def set_dark_title_bar(widget: QWidget, dark_title_bar: bool) -> None:
    # Ensure we're on Windows
    if sys.platform != "win32":
        return

    from ctypes import windll, c_uint32, byref

    # Get Windows build number
    build_number = sys.getwindowsversion().build
    if build_number < 17763:  # Windows 10 1809 minimum
        return

    # Check if the widget's property already matches the setting
    dark_prop = widget.property("DarkTitleBar")
    if dark_prop is not None and dark_prop == dark_title_bar:
        return

    # Set the property (True if dark_title_bar != 0, False otherwise)
    widget.setProperty("DarkTitleBar", dark_title_bar)

    # Load dwmapi.dll and call DwmSetWindowAttribute
    dwmapi = windll.dwmapi
    hwnd = widget.winId()  # Get the window handle
    attribute = 20 if build_number >= 18985 else 19  # Use newer attribute for newer builds
    c_dark_title_bar = c_uint32(dark_title_bar)  # Convert to C-compatible uint32
    dwmapi.DwmSetWindowAttribute(hwnd, attribute, byref(c_dark_title_bar), 4)

    # HACK: Create a 1x1 pixel frameless window to force redraw
    temp_widget = QWidget(None, Qt.FramelessWindowHint)
    temp_widget.resize(1, 1)
    temp_widget.move(widget.pos())
    temp_widget.show()
    temp_widget.deleteLater()  # Safe deletion in Qt event loop

